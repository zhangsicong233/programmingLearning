#include <iostream>

int main() {
  // 比较运算符
  // ==
  int a = 10;
  int b = 20;
  std::cout << (a == b) << std::endl;

  // !=
  std::cout << (a != b) << std::endl;

  // >
  std::cout << (a > b) << std::endl;

  // <
  std::cout << (a < b) << std::endl;

  // >=
  std::cout << (a >= b) << std::endl;

  // <=
  std::cout << (a <= b) << std::endl;

  return 0;
}